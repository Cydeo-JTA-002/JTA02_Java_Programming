package Day21_LoopsPart3_Branching;

public class NestedLoopPractice {
    public static void main(String[] args) {


        for (int i = 1; i <=7 ; i++) {
            System.out.print("#");

            for (int j = 1; j < i ; j++) {
                System.out.print(" ");
            }

            System.out.println("#");

        }




    }
}
