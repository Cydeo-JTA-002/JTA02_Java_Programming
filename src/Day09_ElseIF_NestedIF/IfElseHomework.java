package Day09_ElseIF_NestedIF;

public class IfElseHomework {

    public static void main(String[] args) {

        int num1=10;
        int num2=200;

        // decide which number is bigger

        if (num1>num2) {
            System.out.println(num1 + " is the bigger number");

        }else{
            System.out.println(num2 + " is the bigger number");
        }
        System.out.println("bigger number decider program ended");

    }
}
