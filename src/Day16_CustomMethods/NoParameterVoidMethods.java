package Day16_CustomMethods;

public class NoParameterVoidMethods {


    public static void main(String[] args) {
        printWakeUp();
        printWashYourFaceAndBrushYourTeeth();
        printTidyUpYourBed();
        printGetDressed();
        printHaveBreakfast();
        printCatchTheBus();
    }

    // lets create methods for our daily routine

    // wake up

    public static void printWakeUp(){

        System.out.println("wake up!");
    }

    // wash your face and brush your teeth

    public static void printWashYourFaceAndBrushYourTeeth (){
        System.out.println("wash your face and brush your teeth!");
    }
    //tidy up your bed
    public static void printTidyUpYourBed(){
        System.out.println("Tidy Up your bed!");
    }

    // get dressed for school

    public static void printGetDressed(){
        System.out.println("Get dressed for school!");
    }

    //have breakfast

    public static void printHaveBreakfast(){
        System.out.println("Have Breakfast!");
    }

    // catch the school bus

    public static void printCatchTheBus(){
        System.out.println("Catch the School Bus!");
    }
}
