package Tasks.operatorsTasks;

public class operatorsTask1 {
    public static void main(String[] args) {
        System.out.println(2+5+3*10-2+(3*5));
        System.out.println(29/3);
        System.out.println(29/3.0);
        System.out.println(29%3);

        int counter=0;
        counter++;
        System.out.println("counter = " + counter);
        counter++;
        System.out.println("counter = " + counter);
        counter--;
        System.out.println("counter = " + counter);
    }
}
